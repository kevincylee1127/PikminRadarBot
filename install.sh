# ── Step 4: 設定環境變數 ─────────────────────────────
if [ ! -f .env ]; then
    echo ''
    echo '🔑 請輸入 LINE 金鑰（在 LINE Developers Console 取得）'
    read -p '  LINE_CHANNEL_SECRET: ' LINE_SECRET
    read -p '  LINE_CHANNEL_ACCESS_TOKEN: ' LINE_TOKEN
    cat > .env << ENVEOF
LINE_CHANNEL_SECRET=${LINE_SECRET}
LINE_CHANNEL_ACCESS_TOKEN=${LINE_TOKEN}
OVERPASS_URL=https://overpass-api.de/api/interpreter
SEARCH_RADIUS_M=500
OVERPASS_TIMEOUT_S=10.0
OVERPASS_MAX_RETRIES=3
ENVEOF
    echo '✅ .env 已建立'
else
    echo '✅ .env 已存在，跳過'
fi

# ── Step 5: 建立並啟動 Docker Container ──────────────
echo '🐳 建立 Docker Image（約需 1-2 分鐘）...'
docker build -t pikmin-bot . 2>&1 | tail -5

# 若舊 container 存在則先移除
docker rm -f pikmin-bot 2>/dev/null || true

echo '🚀 啟動 Container...'
docker run -d \
  --name pikmin-bot \
  --restart unless-stopped \
  --env-file .env \
  -p 127.0.0.1:8000:8000 \
  pikmin-bot

sleep 3
echo ''
echo '=== Container 狀態 ==='
